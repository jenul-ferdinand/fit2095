import { Component, OnInit } from '@angular/core';
import { SocketService } from '../services/socket.service';
import { FormsModule } from '@angular/forms';

@Component({
  selector: 'app-aud-to-usd',
  standalone: true,
  imports: [FormsModule],
  templateUrl: './aud-to-usd.component.html',
  styleUrl: './aud-to-usd.component.sass'
})
export class AudToUsdComponent implements OnInit {
  aud: number = 0;
  usd: number = 0;

  constructor (
    private socketService: SocketService
  ) {}

  ngOnInit(): void {
    this.socketService.onConversion().subscribe(
      (data) => {
        const { usd } = data;
        console.log(usd);
        this.usd = usd;
      },
      (error) => {
        console.error('Error receiving converted usd', error);
      }
    );
  }
  
  convertAudToUsd() {
    console.log('Converting to usd:', this.aud)
    this.socketService.requestConversion(this.aud);
}

 














































  






































































































































































































































































































































































































































  convertAudToUSd() {
    this.usd = this.aud * 0.67;
  }
}
